    public class ExclusionStudent2008Map : ClassMap<ExclusionStudent2008>
    {
        public ExclusionStudent2008Map()
        {
            Table("aae_exclusionsstudent_2008");
            Id(x => x.ID).Column("ID");
            Map(x => x.RecordId);
            Map(x => x.LACode);
            Map(x => x.SeedCode);
            Map(x => x.StudentId);
            Map(x => x.ForeName);
            Map(x => x.SurName);
            Map(x => x.ScottishCandidateNumber);
            Map(x => x.StartDate);
            Map(x => x.Gender);
            Map(x => x.StudentStage);
            Map(x => x.StudentStatus);
            Map(x => x.IncidentInClass);
            Map(x => x.RemovedFromRegister);
            Map(x => x.FinishDate);
            Map(x => x.LengthOfExclusion);
            Map(x => x.Appeal);
            Map(x => x.NoProvisionDays);
            Map(x => x.StudentLookedAfter);
            Map(x => x.StudentIncidentId);
        }
    }

